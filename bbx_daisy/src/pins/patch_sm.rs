//! Patch SM (Submodule) pin mappings.
//!
//! The Patch SM is a surface-mount module designed for integration
//! into custom hardware. It's used by the Patch.Init() and other products.
//!
//! # Features
//!
//! - 12 CV inputs (ADC)
//! - 2 CV outputs (DAC)
//! - 4 gate inputs
//! - 2 gate outputs
//! - MIDI input
//! - Stereo audio I/O (PCM3060 codec)
//!
//! # Pin Groups
//!
//! ```text
//! CV_1 through CV_8  - ADC inputs (0-5V or -5V to +5V with offset)
//! CV_9 through CV_12 - Additional ADC inputs
//! CV_OUT_1, CV_OUT_2 - DAC outputs
//! GATE_IN_1 through GATE_IN_4  - Digital gate inputs
//! GATE_OUT_1, GATE_OUT_2       - Digital gate outputs
//! ```

